def _sid() -> str:
    return "session_" + uuid.UUID(int=random.getrandbits(128)).hex[:12]


def _maybe_greet(messages: list[dict[str, str]]) -> None:
    if random.random() < 0.7:
        messages.append({"role": "chatbot", "content": random.choice(GREETINGS_BOT)})
        messages.append({"role": "user", "content": random.choice(GREETINGS_USER)})


def _gen_clean() -> list[dict[str, str]]:
    messages: list[dict[str, str]] = []
    _maybe_greet(messages)
    n_pairs = random.randint(2, 5)
    pairs = [t for t in CLEAN_TURNS if t[0] == "user"]
    sup = [t for t in CLEAN_TURNS if t[0] == "support"]
    for _ in range(n_pairs):
        messages.append({"role": "user", "content": random.choice(pairs)[1]})
        messages.append({"role": "support", "content": random.choice(sup)[1]})
    messages.append({"role": "user", "content": random.choice(CLOSERS_USER)})
    return messages


def _interleave(user_lines: list[str], support_lines: list[str], n: int) -> list[dict[str, str]]:
    """Случайно перемешиваем user-реплики нарушения с ответами оператора/бота."""
    messages: list[dict[str, str]] = []
    _maybe_greet(messages)
    chosen = random.sample(user_lines, k=min(n, len(user_lines)))
    for i, line in enumerate(chosen):
        messages.append({"role": "user", "content": line})
        # иногда две user-реплики подряд (как в реальных данных)
        if random.random() < 0.25 and i + 1 < len(chosen):
            continue
        role = "support" if random.random() < 0.6 else "chatbot"
        messages.append({"role": role, "content": random.choice(support_lines)})
    if random.random() < 0.5:
        messages.append({"role": "user", "content": random.choice(CLOSERS_USER)})
    return messages


GENERATORS = {
    "policy_manipulation": lambda: _interleave(
        POLICY_OPEN + POLICY_PRESS + POLICY_CLOSER, POLICY_SUPPORT, random.randint(3, 6)
    ),
    "adversarial_attack": lambda: _interleave(ADVERSARIAL, ADVERSARIAL_SUPPORT, random.randint(3, 6)),
    "identity_deception": lambda: _interleave(IDENTITY, IDENTITY_SUPPORT, random.randint(3, 6)),
    "transaction_coercion": lambda: _interleave(TRANSACTION, TRANSACTION_SUPPORT, random.randint(3, 6)),
    "information_extraction": lambda: _interleave(INFORMATION, INFORMATION_SUPPORT, random.randint(3, 6)),
    "scope_violation": lambda: _interleave(SCOPE, SCOPE_SUPPORT, random.randint(2, 5)),
}


def main() -> None:
    records: list[dict] = []
    for category, count in TARGET_COUNTS.items():
        for _ in range(count):
            messages = _gen_clean() if category == "clean" else GENERATORS[category]()
            flags = [] if category == "clean" else [{"category": category}]
            records.append(
                {"session_id": _sid(), "messages": messages, "expected_red_flags": flags},
            )
    random.shuffle(records)
    OUT_PATH.write_text(json.dumps(records, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"wrote {len(records)} synthetic dialogues to {OUT_PATH}")


if __name__ == "__main__":
    main()
